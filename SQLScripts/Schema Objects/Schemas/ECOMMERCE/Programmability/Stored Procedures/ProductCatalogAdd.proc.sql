CREATE PROCEDURE [ECOMMERCE].[ProductCatalogAdd]

		@P_PRODUCT_ID	int,	
		@P_CATALOG_ID	int	,
		@P_PRODUCT_ORDER INT
    
AS
BEGIN
	SET NOCOUNT ON;
	
	INSERT INTO [ECOMMERCE].[PRODUCT_CATALOG]
		(
			[PRODUCT_ID],	
			[CATALOG_ID],
			[PRODUCT_ORDER]
		)
	VALUES
		(
			@P_PRODUCT_ID,	
			@P_CATALOG_ID,
			@P_PRODUCT_ORDER	
		)
		
		
		
END
