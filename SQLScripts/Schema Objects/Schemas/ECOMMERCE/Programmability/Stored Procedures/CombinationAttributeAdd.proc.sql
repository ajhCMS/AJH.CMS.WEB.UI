CREATE PROCEDURE [ECOMMERCE].[CombinationAttributeAdd]

	@P_COMBINATION_ID	int,
	@P_ATTRIBUTE_ID	int	
    
AS
BEGIN
	SET NOCOUNT ON;
	
	insert into 
	  [ECOMMERCE].[COMBINATION_ATTRIBUTE]
		(
			[COMBINATION_ID],
			[ATTRIBUTE_ID]
		)
		values 
		(
			@P_COMBINATION_ID,
			@P_ATTRIBUTE_ID
		)
		
	
END
